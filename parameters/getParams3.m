function [pbeta,cbeta,err]=getParams3(h,H)
    pbeta=[
    (H^2*(2*H+3*h(1)))/(6*h(2)*(h(1)+h(2)))

    -((H^2*(2*H+3*(h(1)+h(2))))/(6*h(1)*h(2)))

    (H*(2*H^2+6*h(1)*(h(1)+h(2))+3*H*(2*h(1)+h(2))))/(6*h(1)*(h(1)+h(2)))
    ];
    
    cbeta=[
    H^3/(6*(-H-h(1))*h(1))

    H/2+H^2/(6*h(1))

    H^2/(3*(H+h(1)))+(H*h(1))/(2*(H+h(1)))
    ];

    err=[
    (1/72)*H^2*(3*H^2+6*h(1)*(h(1)+h(2))+4*H*(2*h(1)+h(2)))

    (-(1/72))*H^3*(H+2*h(1))
    ];
end